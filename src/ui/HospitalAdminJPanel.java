    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblDoctor.getModel();
        model.setRowCount(0);
        
        for (Doctor d : history.getHistory()){
            Object[] row =  new Object[6];
            row[0] = d;
            row[1] = d.getDdegree();
            row[2] = d.getDcontact();
            row[3] = d.getDdepartment();
            row[4] = d.getHospitalName();
            row[5] = d.getCityName();
            
            model.addRow(row);
        }
    }

