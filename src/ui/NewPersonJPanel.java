    private void populateDropDownValues() {
        
        ArrayList<String> cityList = new ArrayList<>();
        
        for(Hospital hs: historyH.getHistory()){
            cityList.add(hs.gethCity());
            
        System.out.println("CITY - " +hs.gethCity() );
        }
        
        jCityDropDown1.setModel(new DefaultComboBoxModel<>(cityList.toArray(new String[0])));
        
        
        
        
        
        ArrayList<String> hospList = new ArrayList<>();
        
        for(Hospital hh: historyH.getHistory()){
            hospList.add(hh.gethName());
            
        System.out.println("HOSPITAL - " +hh.gethName());
        }
        
        jHospDropDown1.setModel(new DefaultComboBoxModel<>(hospList.toArray(new String[0])));
        
        
        
    ArrayList<String> docList = new ArrayList<>();
        
        for(Doctor hd: history.getHistory()){
            docList.add(hd.getDname());
            
        System.out.println("Doctor - " +hd.getDname());
       }
        
      jDocDropDown.setModel(new DefaultComboBoxModel<>(docList.toArray(new String[0])));
   }
