    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        if(pname.getText().equals("")||illness.getText().equals("")||city.getText().equals("")||hosp.getText().equals("")||doctor.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Please Enter all data !!");

        }else{

            DefaultTableModel model=(DefaultTableModel) tblDetails.getModel();

            String pn=pname.getText();
            String pi=illness.getText();
            String cc=city.getText();
            String ch=hosp.getText();
            String cd=doctor.getText();

            Patient p = historyP.addNewPatient();

            p.setPatientName(pn);
            p.setPIllness(pi);
            p.setChooseCity(cc);
            p.setChooseHospital(ch);
            p.setChooseDoctor(cd);

            historyP.addNewPatient();

            populateTable();

            JOptionPane.showMessageDialog(this, "\n\n Patient Booking confirmed");
            //Cnic.setText("");
            pname.setText("");
            illness.setText("");
            city.setText("");
            hosp.setText("");
            doctor.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    private void populateTable() {
        
        javax.swing.table.DefaultTableModel model=(javax.swing.table.DefaultTableModel) tblDetails.getModel();
        model.setRowCount(0);
        
        for(Patient p: historyP.getHistoryP()){
            
            Object[] row=new Object[5];
            
            row[0]=p;
            row[1]=p.getPIllness();
            row[2]=p.getChooseCity();
            row[3]=p.getChooseHospital();
            row[4]=p.getChooseDoctor();
            
            model.addRow(row);
        }
    }

